    public void limpar(String campo){
        if(campo == "cliente"){
            clienteCpf.setText("");
            clienteNome.setText("");
            clienteCep.setText("");
            clienteNumero.setText("");
            clienteRua.setText("");
            clienteCidade.setText("");
            clienteUf.setText("");
            clienteEmail.setText("");
            clienteTelefone.setText("");
        }
        else if (campo == "livro"){
            livroNome.setText("");
            livroAno.setText("");
            livroEditora.setText("");
            livroAutor.setText("");
            livroNumPaginas.setText("");
            livroGenero.setSelectedIndex(0);
        }
    }
